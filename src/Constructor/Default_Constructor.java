package Constructor;

public class Default_Constructor {
    int a;
    boolean b;

    public static void main(String[] args) {

        // default constructor
        Default_Constructor obj = new Default_Constructor();
        System.out.println("Defalut value");
        System.out.println("a : " + obj.a);
        System.out.println("b : " + obj.b);
    }

}
